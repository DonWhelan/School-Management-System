    //This is the MouseClick-RemoveAction 
    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
       
        String ans1, ans2;
        Maths m;
    
        ans1 = txtAns1.getText();
        ans2 = txtAns2.getText();

       
         for (int i = 0; i < Count; i++) {
           m = mat[i];
          if (ans1.equals(m.getAns1()) && ans2.equals(m.getAns2())) {
             mat[i] = mat[Count - 1];
             mat[Count - 1] = null;
               Count--;
             
             
                }
            }
         JOptionPane.showMessageDialog(null, "deleted.", "Gone", JOptionPane.PLAIN_MESSAGE);
             
        
    }//GEN-LAST:event_btnRemoveActionPerformed
