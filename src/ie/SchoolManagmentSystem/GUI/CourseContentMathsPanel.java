    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
  
        String ans1, ans2;
        Maths m;
    
        ans1 = txtAns1.getText();
        ans2 = txtAns2.getText();
  
     
        m = new Maths (ans1, ans2);

        JOptionPane.showMessageDialog(null, "Successfully added Answers.", "Congratulations", JOptionPane.PLAIN_MESSAGE);
        
        mat[Count] = m;
        Count++; 
     
        txtAns1.setText("");
        txtAns2.setText("");
        
    }//GEN-LAST:event_btnSaveActionPerformed
