    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String ans1, ans2;
        Maths m;
    
        ans1 = txtAns1.getText();
        ans2 = txtAns2.getText();           
        

            //Finding the match
        for (int i=0; i < Count; i++){   
            m = mat[i];
            if (ans1.equals(m.getAns1()) && ans2.equals(m.getAns2())){
            ans1 = m.getAns1();
            ans2 = m.getAns2();
                   
            
            txtAns1.setText("");
            txtAns2.setText(""); 
            
            }   
            

                
        JOptionPane.showMessageDialog(null, "Answer one " + ans1 + "Answer two :" + ans2,"Attention!", JOptionPane.PLAIN_MESSAGE);            

            txtAns1.setText("");
             txtAns2.setText(""); 
                 return;
                }
             
             
    }//GEN-LAST:event_btnSearchActionPerformed
